    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        listEmployee = employeeDAO.searchEmployee(txtName.getText().trim());
        if (listEmployee.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Danh sách trống");
        } else {
            String[][] data = new String[listEmployee.size()][5];
            for(int i=0 ; i<listEmployee.size();i++){
                var nhanVien = listEmployee.get(i);
                data[i][0] = nhanVien.getId()+"";
                data[i][1] = nhanVien.getName();
                data[i][2] = nhanVien.getEmail();
                data[i][3] = nhanVien.getPhoneNumber();
                data[i][4] = nhanVien.getAddress();
            }
        
            String[] cols = {"MNV", "Tên", "Email", "Sđt", "Địa chỉ"};
            tm = new DefaultTableModel(data, cols);
            tblEmployee.setModel(tm);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

