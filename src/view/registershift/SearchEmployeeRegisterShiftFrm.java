    private void tblEmployeeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEmployeeMouseClicked
        // TODO add your handling code here:
        int row = tblEmployee.getSelectedRow();
        if (row >= 0 && row <= tblEmployee.getRowCount()-1) {
            int id = Integer.parseInt(tm.getValueAt(row, 0).toString());
            String name = tm.getValueAt(row, 1).toString();
            String email = tm.getValueAt(row, 2).toString();
            String phoneNumber = tm.getValueAt(row, 3).toString();
            String address = tm.getValueAt(row, 4).toString();
            Employee employee = new Employee(id, name, email, phoneNumber, address);
            (new RegisterShiftFrm(employee)).setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_tblEmployeeMouseClicked
    
